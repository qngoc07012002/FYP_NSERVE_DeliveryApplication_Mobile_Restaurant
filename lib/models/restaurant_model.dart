class Restaurant {
  final String id;
  final bool isOpen;
  final String name;
  final String description;
  final String image;
  final double rating;
  final double balance;

  Restaurant({
    required this.id,
    required this.isOpen,
    required this.name,
    required this.description,
    required this.image,
    required this.rating,
    required this.balance,
  });
}
